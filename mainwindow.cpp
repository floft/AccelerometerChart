void MainWindow::realtimeDataSlot()
{
  // calculate two new data points:
  double key = QDateTime::currentDateTime().toMSecsSinceEpoch()/1000.0;
  static double lastPointKey = 0;
  if (key-lastPointKey > 0.01) // at most add point every 10 ms
  {
    //double value0 = qSin(key); //qSin(key*1.6+qCos(key*1.7)*2)*10 + qSin(key*1.2+0.56)*20 + 26;
    //double value1 = qCos(key); //qSin(key*1.3+qCos(key*1.2)*1.2)*7 + qSin(key*0.9+0.26)*24 + 26;
    //double value2 = qTan(key); //qSin(key*1.3+qCos(key*1.2)*1.2)*7 + qSin(key*0.9+0.26)*24 + 26;
    // add data to lines:
    const QAccelerometerReading* reading = accelerometer->reading();

    if (reading)
    {
      x = reading->x();
      y = reading->y();
      z = reading->z();
    }

    ui->customPlot->graph(0)->addData(key, x);
    ui->customPlot->graph(1)->addData(key, y);
    ui->customPlot->graph(2)->addData(key, z);
    // remove data from a while back
    ui->customPlot->graph(0)->removeDataBefore(key-8*100);
    ui->customPlot->graph(1)->removeDataBefore(key-8*100);
    ui->customPlot->graph(2)->removeDataBefore(key-8*100);
    // rescale value (vertical) axis to fit the current data:
    //ui->customPlot->graph(0)->rescaleValueAxis();
    //ui->customPlot->graph(1)->rescaleValueAxis();
    //ui->customPlot->graph(2)->rescaleValueAxis(true);
    lastPointKey = key;
  }

  // make key axis range scroll with the data (at a constant range size of 8)
  // but only if we haven't scrolled to the left
  static bool first = true;
  QCPRange xrange = ui->customPlot->xAxis->range();
  if (xrange.upper >= key-0.5 || first)
    ui->customPlot->xAxis->setRange(key+0.25, xrange.upper-xrange.lower, Qt::AlignRight);
  first = false;

  ui->customPlot->replot();

  // calculate frames per second:
  static double lastFpsKey;
  static int frameCount;
  ++frameCount;

  if (key-lastFpsKey > 2) // average fps over 2 seconds
  {
    ui->statusBar->showMessage(
          QString("%1 FPS, Total Data points: %2")
          .arg(frameCount/(key-lastFpsKey), 0, 'f', 0)
          .arg(ui->customPlot->graph(0)->data()->count()+ui->customPlot->graph(1)->data()->count())
          , 0);
    lastFpsKey = key;
    frameCount = 0;
  }
}
